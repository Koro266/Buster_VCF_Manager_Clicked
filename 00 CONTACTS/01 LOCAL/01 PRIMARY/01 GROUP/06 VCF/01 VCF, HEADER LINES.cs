//___________________________________________________________________________________________________________________________________________________
//GLOBAL
using VCF_TEXT	= CONTACTS.GLOBAL.VCF.VcfText;

//___________________________________________________________________________________________________________________________________________________
namespace CONTACTS.LOCAL.PRIMARY.GROUP.VCF
{
	//___________________________________________________________________________________________________________________________________________
	public class Vcf_01_HeaderLines
	{
		private const string BEGIN		= "BEGIN:VCARD";
		private const string VERSION	= "VERSION:3.0";
		private const string PROD_ID	= "PRODID:-//Apple Inc.//macOS 11.2.1//EN";

		//___________________________________________________________________________________________________________________________________________
		public Vcf_01_HeaderLines()
		{
		}
		//___________________________________________________________________________________________________________________________________________
		public void AppendLines( VCF_TEXT vcf_text )
		{
			vcf_text.NextIndex = BEGIN;
			vcf_text.NextIndex = VERSION;
			vcf_text.NextIndex = PROD_ID;
		}
	}
}
