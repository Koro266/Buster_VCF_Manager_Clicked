namespace CONTACTS.LOCAL.TERTIARY.ADDRESS
{
	//___________________________________________________________________________________________________________________________________________
	public partial class Database
	{
		//___________________________________________________________________________________________________________________________________________
		public partial class Select
		{
			//___________________________________________________________________________________________________________________________________________
			public partial class Unique
			{
				//___________________________________________________________________________________________________________________________________
				/// <summary>
				/// Returns all unique province names from TDF_Addresses.
				/// </summary>
				public class ProvinceNames : UNIQUE
				{
					private const string sql_text =
					@"
						SELECT DISTINCT 
							TDF_Addresses.st_ProvinceName
						FROM 
							TDF_Addresses
						WHERE 
							((st_ProvinceName Is Not Null AND Len(RTrim(LTrim(st_ProvinceName))) > 0))
						ORDER 
							BY TDF_Addresses.st_ProvinceName;
					";

					//_______________________________________________________________________________________________________________________________
					public ProvinceNames() : base( sql_text )
					{
					}
				}
			}
		}
	}
}
