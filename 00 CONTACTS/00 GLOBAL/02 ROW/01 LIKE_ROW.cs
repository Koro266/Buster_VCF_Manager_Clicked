namespace CONTACTS.GLOBAL.DATABASE.ROW
{
	//_______________________________________________________________________________________________________________________________________
	public class LikeRow
	{
		private int pk_Row;
		private string st_Value;

		//___________________________________________________________________________________________________________________________________
		public LikeRow( int pk, string st )
		{
			this.pk_Row = pk;
			this.st_Value = st;
		}
		//___________________________________________________________________________________________________________________________________
		public int PkRow { get { return pk_Row; } }
		public string LikeValue { get { return st_Value; } }
		public override string ToString() { return st_Value; }
	}
}
